function 角度算四方向(角)
  local 方向 = 0
  if(角 >0 and 角 < 91) then
    方向 = 0 --"东北"
  elseif(角 > 90 and 角 < 181) then
    方向 = 1 --"西北"
  elseif(角 > 180 and 角 < 271) then
    方向 = 2 --"西南"
  elseif(角 > 270 or  角 == 0) then
    方向 = 3 --"东南"
  end
  return 方向
end

