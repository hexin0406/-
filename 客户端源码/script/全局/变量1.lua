随机序列=0
function 取随机数(q,w)
  随机序列=随机序列+1
  if 随机序列>=1000 then
    随机序列=0
  end
  if q==nil or w==nil then
    q=1 w=100
  else
  end
  math.randomseed(tostring(os.clock()*os.time()*随机序列))
  return  math.random(math.floor(q),math.floor(w))
end

