function 商城处理类:商城取法宝(名称,等级)
  local 识别码=os.time().."_"..os.time().."_"..取随机数(1000,9999999).."_"..随机序列
  随机序列=随机序列+1
  local 法宝数据={}
  local 道具 = 取物品数据(名称)
  法宝数据.名称=名称
  法宝数据.总类=1000
  法宝数据.分类=道具[3]
  法宝数据.使用=道具[5]
  法宝数据.特技=道具[6]
  法宝数据.气血=0
  法宝数据.魔法=取灵气上限(道具[3])
  法宝数据.角色限制=道具[7] or 0
  法宝数据.五行=取五行()
  法宝数据.伤害=道具[8] or 0
  法宝数据.当前经验=0
  法宝数据.升级经验=法宝经验[法宝数据.分类][1]
  法宝数据.识别码=识别码
  法宝数据.价格=等级*(f函数.读配置(程序目录.."Shopping.ini","法宝","法宝")+0)
  return 法宝数据
end

function 商城处理类:数据处理(id,内容)
	self.允许购买 = true
	self.购买组号 = 内容.组号 + 0
	self.购买编号 = 内容.编号
	self.购买数量 = 内容.数量
	self.购买分类 = 内容.分类
	local 仙玉=f函数.读配置(程序目录..[[data\]]..玩家数据[id].账号..[[\账号信息.txt]],"账号配置","仙玉")+0
	local 银子=玩家数据[id].角色.数据.银子
	local 元宝=玩家数据[id].角色.数据.元宝.当前元宝
	local 积分=玩家数据[id].角色.数据.比武积分.当前积分
	local 当前活跃=玩家数据[id].角色.数据.累积活跃.当前积分
	if self.购买数量 > 99 then
		发送数据(玩家数据[id].连接id, 7, "#y/您的购买的数量超过99个!")
		return 0
	end
	if self.购买数量 ==0 then
		发送数据(玩家数据[id].连接id, 7, "#y/请输入正确的数量")
		return 0
	end
	if self.购买数量 == nil or self.购买分类== "宝宝" or self.购买分类== "金元宝宠物" then
		self.购买数量 = 1
	elseif self.购买数量 == nil or (self.购买数量 ~= nil and self.购买数量 < 1) then
		self.购买数量 = 1
	elseif self.商品组[self.购买组号][self.购买分类][self.购买编号].可叠加 then
		self.购买总量 = 内容.数量
