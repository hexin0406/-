function 商城处理类:取商品组数据(id, 序号,积分商城)
	if  玩家数据[id].角色.数据.元宝==nil then
		玩家数据[id].角色.数据.元宝={}
	end
	if  玩家数据[id].角色.数据.元宝.当前元宝==nil then
		玩家数据[id].角色.数据.元宝={当前元宝=0,累积元宝=0}
	end
	self.发送消息 = {}
	序号 = 序号 + 0
	local 妖魔兑换积分=0
	local 副本积分=0
	local 种族积分=0
	local 累积活跃=0
	local 当前活跃=0
	if 玩家数据[id].角色.数据.副本积分~=nil then
	    副本积分=玩家数据[id].角色.数据.副本积分
	end
	if 妖魔积分~=nil and 妖魔积分[id]~=nil then
		妖魔兑换积分=妖魔积分[id].当前
	end
	if 玩家数据[id].角色.数据.累积活跃~=nil then
	    累积活跃=玩家数据[id].角色.数据.累积活跃.总积分
	    当前活跃=玩家数据[id].角色.数据.累积活跃.当前积分
	end

	self.商品组[序号].银子 = 玩家数据[id].角色.数据.银子
	self.商品组[序号].积分数 = {比武积分=玩家数据[id].角色.数据.比武积分.当前积分,种族积分=种族积分,副本积分=副本积分,妖魔积分=妖魔兑换积分,累积活跃=累积活跃,当前活跃=当前活跃}
	self.商品组[序号].组号 = 序号
	self.商品组[序号].仙灵玉 = f函数.读配置(程序目录..[[data\]]..玩家数据[id].账号..[[\账号信息.txt]],"账号配置","仙玉")+0
	self.商品组[序号].元宝 = 玩家数据[id].角色.数据.元宝.当前元宝
	self.商品组[序号+5].银子 = 玩家数据[id].角色.数据.银子
	self.商品组[序号+5].积分数 = {比武积分=玩家数据[id].角色.数据.比武积分.当前积分,种族积分=种族积分,副本积分=副本积分,妖魔积分=妖魔兑换积分,累积活跃=累积活跃,当前活跃=当前活跃}
	self.商品组[序号+5].组号 = 序号
	self.商品组[序号+5].仙灵玉 = f函数.读配置(程序目录..[[data\]]..玩家数据[id].账号..[[\账号信息.txt]],"账号配置","仙玉")+0
	self.商品组[序号+5].元宝 = 玩家数据[id].角色.数据.元宝.当前元宝
	if 积分商城~=nil then
	    self.商品组[序号].序列=2
	    self.商品组[序号+5].序列=2
	else
		self.商品组[序号].序列=1
		self.商品组[序号+5].序列=1
	end
	if 序号==18 then
		发送数据(玩家数据[id].连接id, 3700, self.商品组[序号+5])
	elseif 序号==19 then
		发送数据(玩家数据[id].连接id, 3701, self.商品组[序号+5])
	end

end
