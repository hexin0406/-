	local 购买价格 = self.商品组[self.购买组号][self.购买分类][self.购买编号].价格
	local 购买总价 = self.商品组[self.购买组号][self.购买分类][self.购买编号].价格*内容.数量
	if self.购买分类== "宝宝" then
		if #玩家数据[id].召唤兽.数据 >= 7 then
			发送数据(玩家数据[id].连接id, 7, "#y/您当前无法携带更多的召唤兽了")
